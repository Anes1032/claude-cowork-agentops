#!/usr/bin/env python3
"""
Proposal adoption evaluation (AgentOps: feedback loop).

Classifies each CLAUDE.md the housekeeping produced by its git state and computes
an acceptance rate, quantifying whether the pipeline's output is useful.

States:
  - committed : tracked in git = the user reviewed and accepted (committed)
  - pending   : exists but untracked = awaiting review
  - nongit    : outside any git repo = manual review only (excluded from the rate)
acceptance_rate = committed / (committed + pending)

CLAUDE.md generated by this pipeline carry a provenance marker
("claude-mem-housekeeping"), so a pipeline-only rate is reported separately.

Usage:
  python3 adoption_eval.py --print
  python3 adoption_eval.py --vault "<vault>" [--date YYYY-MM-DD] [--machine NAME]
      -> writes <vault>/adoption/<YYYY-MM>/<DD>/<machine>.md
"""
import argparse
import datetime as dt
import glob
import json
import os
import subprocess
import sys

HK_DIR = os.path.dirname(os.path.abspath(__file__))
PROJECTS_ROOT = os.path.dirname(HK_DIR)  # parent dir = projects root


def _cfg():
    for base in [os.path.expanduser("~/.claude-mem")] + sorted(glob.glob("/sessions/*/mnt/.claude-mem")):
        p = os.path.join(base, "machine.json")
        if os.path.exists(p):
            try:
                return json.load(open(p))
            except Exception:
                pass
    return {}


def machine():
    return str(_cfg().get("machine") or "unknown")


def run(cmd, cwd=None):
    return subprocess.run(cmd, cwd=cwd, capture_output=True, text=True)


def find_repo_root(path):
    d = os.path.dirname(path)
    while True:
        if os.path.isdir(os.path.join(d, ".git")):
            return d
        parent = os.path.dirname(d)
        if parent == d:
            return None
        d = parent


PROVENANCE = "claude-mem-housekeeping"  # marker embedded in pipeline-generated CLAUDE.md


def is_pipeline(claude_md):
    try:
        with open(claude_md, errors="ignore") as f:
            return PROVENANCE in f.read()
    except Exception:
        return False


def classify(claude_md):
    pipe = is_pipeline(claude_md)
    repo = find_repo_root(claude_md)
    if not repo:
        return dict(path=claude_md, state="nongit", repo=None, last_commit=None, pipeline=pipe)
    rel = os.path.relpath(claude_md, repo)
    tracked = run(["git", "ls-files", "--error-unmatch", rel], cwd=repo).returncode == 0
    if tracked:
        lc = run(["git", "log", "-1", "--format=%cs", "--", rel], cwd=repo).stdout.strip()
        return dict(path=claude_md, state="committed", repo=repo, last_commit=lc or None, pipeline=pipe)
    return dict(path=claude_md, state="pending", repo=repo, last_commit=None, pipeline=pipe)


def collect(projects_root):
    items = []
    for cm in glob.glob(os.path.join(projects_root, "**", "CLAUDE.md"), recursive=True):
        if "/node_modules/" in cm or "/claude-cowork-agentops/" in cm:
            continue
        # Skip CLAUDE.md inside an Obsidian vault (ancestor with a .obsidian dir).
        d = os.path.dirname(cm)
        in_vault = False
        while d and d != projects_root and len(d) > 1:
            if os.path.isdir(os.path.join(d, ".obsidian")):
                in_vault = True
                break
            d = os.path.dirname(d)
        if in_vault:
            continue
        items.append(classify(cm))
    counts = {"committed": 0, "pending": 0, "nongit": 0}
    pcounts = {"committed": 0, "pending": 0, "nongit": 0}
    for it in items:
        counts[it["state"]] += 1
        if it["pipeline"]:
            pcounts[it["state"]] += 1
    denom = counts["committed"] + counts["pending"]
    rate = round(counts["committed"] / denom, 3) if denom else None
    pdenom = pcounts["committed"] + pcounts["pending"]
    prate = round(pcounts["committed"] / pdenom, 3) if pdenom else None
    return items, counts, rate, pcounts, prate


def render(items, counts, rate, pcounts, prate, date_str, mach, projects_root):
    pdenom = pcounts["committed"] + pcounts["pending"]
    fm = [
        "---", "type: adoption", f"date: {date_str}", f"machine: {mach}",
        f"claudemd_total: {len(items)}",
        f"claudemd_committed: {counts['committed']}",
        f"claudemd_pending: {counts['pending']}",
        f"claudemd_nongit: {counts['nongit']}",
        f"acceptance_rate: {rate if rate is not None else 'null'}",
        f"pipeline_committed: {pcounts['committed']}",
        f"pipeline_pending: {pcounts['pending']}",
        f"pipeline_acceptance_rate: {prate if prate is not None else 'null'}",
        "tags: [adoption, agentops, claude-mem]", "---",
    ]
    b = [f"# Proposal adoption - {date_str} @ {mach}", ""]
    pct = f"{rate*100:.0f}%" if rate is not None else "-"
    b.append(f"- CLAUDE.md acceptance (overall): **{pct}** (committed {counts['committed']} / pending {counts['pending']})")
    if pdenom:
        ppct = f"{prate*100:.0f}%" if prate is not None else "-"
        b.append(f"- pipeline-generated acceptance: **{ppct}** (committed {pcounts['committed']} / pending {pcounts['pending']})")
    else:
        b.append("- pipeline-generated (provenance-marked): none yet (counted from future runs)")
    b.append(f"- total {len(items)} (nongit {counts['nongit']} excluded from rate / manual review)")
    b.append("")
    b.append("| file | state | origin | commit date |")
    b.append("|---|---|---|---|")
    order = {"pending": 0, "committed": 1, "nongit": 2}
    for it in sorted(items, key=lambda x: (order[x["state"]], x["path"])):
        rel = os.path.relpath(it["path"], projects_root)
        state = {"committed": "accepted", "pending": "pending", "nongit": "nongit"}[it["state"]]
        origin = "pipeline" if it["pipeline"] else "manual"
        b.append(f"| {rel} | {state} | {origin} | {it['last_commit'] or '-'} |")
    b.append("")
    return "\n".join(fm) + "\n\n" + "\n".join(b) + "\n"


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--projects-root", default=PROJECTS_ROOT)
    ap.add_argument("--date", default=dt.date.today().isoformat())
    ap.add_argument("--machine")
    ap.add_argument("--vault")
    ap.add_argument("--print", dest="do_print", action="store_true")
    args = ap.parse_args()

    items, counts, rate, pcounts, prate = collect(args.projects_root)
    mach = args.machine or machine()
    note = render(items, counts, rate, pcounts, prate, args.date, mach, args.projects_root)

    if args.vault:
        ym, dd = args.date[:7], args.date[8:10]
        out_dir = os.path.join(args.vault, "adoption", ym, dd)
        os.makedirs(out_dir, exist_ok=True)
        out = os.path.join(out_dir, f"{mach}.md")
        with open(out, "w") as f:
            f.write(note)
        print(f"wrote: {out}")
    if args.do_print or not args.vault:
        print(note)


if __name__ == "__main__":
    main()
